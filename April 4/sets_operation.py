A = {5, 3, 8, 6, 1}
B = {1, 5, 3, 4, 2}

union = A.union(B)
print(union)

intersection = A.intersection(B)
print(intersection)

difference = A.difference(B)
print(difference)

print("Maximum in set A",max(A))
print("Minimum in set A",min(A))

print("Maximum in set B",max(B))
print("Minimum in set B",min(B))